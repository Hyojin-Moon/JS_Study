/**
 * 변수 선언
 * 
 * 1) var - 더이상 쓰지않는다.
 * 2) let -
 * var, let으로 선언하면 값을 추후 변경할 수 있다.
 * 3) const -
 * const는 값을 변경할 수 없다.
 */
var name = '문터프';
console.log(name);
var age = 36;
console.log(age);

let ive = '문효진';
console.log(ive);
ive = '장성용';
console.log(ive);
console.log('-------------')

const one = '문효진';
console.log(one);
//const one = '장성용';
console.log(one);

/**
 * 선언과 할당
 * 1) 변수를 선언하는 것
 * 2) 할당
 */

